//Common Binary numbers
#include <iostream>
using namespace std;

int main() {
    cout << "Common Binary Numbers:" << endl;
    cout << "0 = 0" << endl;
    cout << "1 = 1" << endl;
    cout << "10 = 2" << endl;
    cout << "11 = 3" << endl;
    cout << "100 = 4" << endl;
    cout << "101 = 5" << endl;
    cout << "110 = 6" << endl;
    cout << "111 = 7" << endl;
    cout << "1000 = 8" << endl;
    cout << "1001 = 9" << endl;
    cout << "1010 = 10" << endl;
    return 0;
}